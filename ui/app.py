def _calculate_segment_steps(distance: int, stats: dict, max_efficiency: float = 0.60) -> int:
    """
    Calculate expected steps for a single route segment.

    Uses the travel formula confirmed against the official Walkscape tool:
    1. adjusted = distance / (2 + WE)  -- base efficiency is 200% for traveling, NO WE cap
    2. per_action = adjusted / 10
    3. per_action = per_action * (1 + pct) + flat
    4. per_action = max(10, ceil(per_action))
    5. expected_total = ceil(10 / (1 + DA) * per_action)

    Stats dict should already include level bonus WE from aggregate_gearset_stats.
    """
    import math
    we = stats.get('work_efficiency', 0.0)
    da = stats.get('double_action', 0.0)
    flat = stats.get('steps_add', 0)
    pct = stats.get('steps_percent', 0.0)

    # Step 1: Apply WE with base efficiency of 200% (2.0) for traveling
    adjusted = distance / (2.0 + we)

    # Step 2: Divide into 10 action nodes
    per_action = adjusted / 10.0

    # Step 3: Apply percentage and flat step modifiers
    per_action = per_action * (1.0 + pct) + flat

    # Step 4: Minimum 10 steps per action, round up
    per_action = max(10, math.ceil(per_action))

    # Step 5: Expected total with DA
    expected_paid = 10.0 / (1.0 + da)
    return math.ceil(expected_paid * per_action)


