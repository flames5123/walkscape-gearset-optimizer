@app.get("/api/optimization-settings")
async def get_optimization_settings(request: Request):
    """
    Get optimization settings including available sorting options.

    Returns:
        Available sorting metrics for activities and recipes,
        plus current user preferences from session.
        current_order is a list of [metric_key, weight] tuples where
        weight is an integer 0-100.
    """
    from util.walkscape_constants import Sorting, SortingType

    # Get session
    session_uuid = request.state.session_uuid
    session = get_db().get_session(session_uuid)

    # Get current preferences from session
    ui_config = session.get('ui_config', {}) if session else {}
    activity_sorting = ui_config.get('optimize_sorting_activity', [])
    recipe_sorting = ui_config.get('optimize_sorting_recipe', [])
    activity_include_consumables = ui_config.get('activity_include_consumables', False)
    recipe_include_consumables = ui_config.get('recipe_include_consumables', False)

    def normalize_sorting(sorting_list):
        """Convert plain key arrays to [key, 100] tuples if needed."""
        if not sorting_list:
            return []
        # Check if old format (plain strings) or new format ([key, weight] tuples)
        if sorting_list and isinstance(sorting_list[0], str):
            # Old format: plain key array -> convert to [key, 100] tuples
            return [[key, 100] for key in sorting_list]
        # Already in tuple format, ensure weights are clamped ints
        result = []
        for entry in sorting_list:
            if isinstance(entry, (list, tuple)) and len(entry) == 2:
                key = entry[0]
                weight = max(0, min(100, int(entry[1])))
                result.append([key, weight])
            elif isinstance(entry, str):
                # Mixed format fallback
                result.append([entry, 100])
        return result

    activity_sorting = normalize_sorting(activity_sorting)
    recipe_sorting = normalize_sorting(recipe_sorting)

    # Build available sorting options
    activity_options = []
    recipe_options = []

    # Quality-specific metrics (have "X Quality" in display name)
    quality_specific_keys = ['materials_for_target', 'steps_for_target', 'total_crafts']

    for sorting in Sorting:
        option = {
            'key': sorting.metric_key,
            'name': sorting.name,
            'display_name': sorting.display_name,
            'is_reverse': sorting.is_reverse,
            'requires_quality': sorting.metric_key in quality_specific_keys
        }

        # Check which types this sorting applies to
        if SortingType.ACTIVITY in sorting.types:
            activity_options.append(option)
        if SortingType.RECIPE in sorting.types:
            recipe_options.append(option)

    # If no preferences saved, use default order with weight 100
    # If preferences exist, only append genuinely NEW metrics (not ones the user removed)
    removed_activity = set(ui_config.get('optimize_removed_activity', []))
    removed_recipe = set(ui_config.get('optimize_removed_recipe', []))

    if not activity_sorting:
        activity_sorting = [[opt['key'], 100] for opt in activity_options]
    else:
        saved_keys = set(entry[0] for entry in activity_sorting)
        for opt in activity_options:
            if opt['key'] not in saved_keys and opt['key'] not in removed_activity:
                activity_sorting.append([opt['key'], 100])

    if not recipe_sorting:
        recipe_sorting = [[opt['key'], 100] for opt in recipe_options]
    else:
        saved_keys = set(entry[0] for entry in recipe_sorting)
        for opt in recipe_options:
            if opt['key'] not in saved_keys and opt['key'] not in removed_recipe:
                recipe_sorting.append([opt['key'], 100])

    return {
        'activity': {
            'options': activity_options,
            'current_order': activity_sorting,
            'include_consumables': activity_include_consumables
        },
        'recipe': {
            'options': recipe_options,
            'current_order': recipe_sorting,
            'include_consumables': recipe_include_consumables
        }
    }



@app.post("/api/optimization-settings")
async def save_optimization_settings(request: Request):
    """
    Save optimization sorting preferences to session.

    Request body:
        activity_sorting: List of [metric_key, weight] tuples where weight is int 0-100
        recipe_sorting: List of [metric_key, weight] tuples where weight is int 0-100
        activity_include_consumables: bool
        recipe_include_consumables: bool
    """
    body = await request.json()
    activity_sorting = body.get('activity_sorting', [])
    recipe_sorting = body.get('recipe_sorting', [])
    activity_include_consumables = body.get('activity_include_consumables', False)
    recipe_include_consumables = body.get('recipe_include_consumables', False)

    def validate_sorting_tuples(sorting_list):
        """Validate and normalize sorting entries to [key, weight] tuples with clamped weights."""
        result = []
        for entry in sorting_list:
            if isinstance(entry, str):
                # Old format: plain key string -> convert to [key, 100]
                result.append([entry, 100])
            elif isinstance(entry, (list, tuple)) and len(entry) == 2:
                key = entry[0]
                try:
                    weight = int(entry[1])
                except (ValueError, TypeError):
                    weight = 100
                weight = max(0, min(100, weight))
                result.append([key, weight])
            # Skip malformed entries
        return result

    activity_sorting = validate_sorting_tuples(activity_sorting)
    recipe_sorting = validate_sorting_tuples(recipe_sorting)

    session_uuid = request.state.session_uuid
    log(f"Saving optimization settings:", session_uuid)
    log(f"  Activity sorting: {activity_sorting}", session_uuid)
    log(f"  Recipe sorting: {recipe_sorting}", session_uuid)
    log(f"  Activity include consumables: {activity_include_consumables}", session_uuid)
    log(f"  Recipe include consumables: {recipe_include_consumables}", session_uuid)

    # Get session
    session = get_db().get_session(session_uuid)
    if not session:
        raise HTTPException(status_code=404, detail="Session not found")

    # Update UI config directly (not using update_config_path which has path logic)
    ui_config = session.get('ui_config', {})
    ui_config['optimize_sorting_activity'] = activity_sorting
    ui_config['optimize_sorting_recipe'] = recipe_sorting
    ui_config['activity_include_consumables'] = activity_include_consumables
    ui_config['recipe_include_consumables'] = recipe_include_consumables

    # Track which metrics the user has explicitly removed so the GET endpoint
    # doesn't re-add them as "new" metrics on reload
    from util.walkscape_constants import Sorting, SortingType
    all_activity_keys = {s.metric_key for s in Sorting if SortingType.ACTIVITY in s.types}
    all_recipe_keys = {s.metric_key for s in Sorting if SortingType.RECIPE in s.types}
    saved_activity_keys = {entry[0] for entry in activity_sorting if isinstance(entry, (list, tuple))}
    saved_recipe_keys = {entry[0] for entry in recipe_sorting if isinstance(entry, (list, tuple))}
    ui_config['optimize_removed_activity'] = list(all_activity_keys - saved_activity_keys)
    ui_config['optimize_removed_recipe'] = list(all_recipe_keys - saved_recipe_keys)

    get_db().update_ui_config(session_uuid, ui_config)

    log(f"✓ Optimization settings saved", session_uuid)

    return {
        'success': True,
        'message': 'Optimization settings saved'
    }


