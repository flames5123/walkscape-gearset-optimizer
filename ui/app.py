        skill_value = skill.value.lower()
        if skill_value in gathering_skills:
            category = 'Gathering'
        elif skill_value in artisan_skills:
            category = 'Artisan'
        elif skill_value in utility_skills:
            category = 'Utility'
        else:
            # Skip skills that aren't in any category (like Traveling)
            continue
        
