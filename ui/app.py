class BroadcastDismissData(BaseModel):
    """Broadcast dismiss request model."""
    broadcast_id: str


@app.post("/api/broadcast/dismiss")
async def dismiss_broadcast(request: Request, data: BroadcastDismissData):
    """
    Dismiss a broadcast for the current session.

    Reads session UUID from cookie, broadcast_id from body.
    Handles duplicate dismissals gracefully (returns success).

    Requirements: 9.6
    """
    session_uuid = getattr(request.state, 'session_uuid', None)
    if not session_uuid:
        raise HTTPException(
            status_code=400,
            detail={
                "error": True,
                "code": "MISSING_SESSION",
                "message": "No session UUID found"
            }
        )

    try:
        get_db().dismiss_broadcast(data.broadcast_id, session_uuid)
        return {"success": True}
    except Exception as e:
        raise HTTPException(
            status_code=500,
            detail={
                "error": True,
                "code": "DISMISS_ERROR",
                "message": str(e)
            }
        )


@app.get("/api/debug-status")
async def get_debug_status(request: Request):
    """
    Check if debug mode is enabled for the current session.

    Reads session UUID from cookie and checks the debug_sessions table.

    Returns:
        JSON with enabled: true/false

    Requirements: 5.1, 5.2
    """
    session_uuid = getattr(request.state, 'session_uuid', None)
    if not session_uuid:
        return {"enabled": False}

    try:
        enabled = get_db().is_debug_enabled(session_uuid)
        return {"enabled": enabled}
    except Exception as e:
        # Fail silently — debug mode defaults to off
        return {"enabled": False}

