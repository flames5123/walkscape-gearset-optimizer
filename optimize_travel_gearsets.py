def optimize_for_route(segments, character, sorting_priority=None, hidden_items=None, include_consumables=False):
    """
    Entry point for UI-triggered travel optimization.
    
    Args:
        segments: List of segment dicts from the API (from /api/travel/route)
        character: Character object
        sorting_priority: List of Sorting enum objects for optimization priority
        hidden_items: Set of items to exclude from optimization
        include_consumables: Whether to include consumables (not yet supported for travel)
    
    Returns:
        Tuple of (gearset_dict, metrics_dict, iterations)
    """
    global SORTING_PRIORITY, IGNORED_ITEMS
    
    # Apply sorting priority if provided
    if sorting_priority:
        SORTING_PRIORITY = sorting_priority
    
    # Apply hidden items if provided
    if hidden_items:
        IGNORED_ITEMS = hidden_items
    
    # Get tool slots based on character level
    total_steps_char = sum(character.skills.values())
    char_level = character_level_from_steps(total_steps_char)
    max_tool_slots = tool_slots_for_level(char_level)
    
    # Define all slots
    gear_slots = ['head', 'cape', 'neck', 'chest', 'hands', 'legs', 'feet',
                  'ring1', 'ring2', 'back', 'primary', 'secondary']
    tool_slots = [f'tool{i}' for i in range(max_tool_slots)]
    all_slots = gear_slots + tool_slots
    
    # Convert segments to routes for item filtering
    routes = _segments_to_routes(segments)
    
    # Get all items with travel stats
    all_items = []
    for item, qty in character.items.items():
        if qty == 0 or not hasattr(item, 'slot'):
            continue
        if item in IGNORED_ITEMS:
            continue
        
        # Check if unlocked
        if hasattr(item, 'is_unlocked'):
            if not item.is_unlocked(character, ignore_gear_requirements=True):
                continue
        
        # Check if has travel stats for any segment's starting location
        has_stats = False
        for route in routes:
            stats = item.get_stats_for_skill('travel', location=route[0], character=character)
            if any(stats.get(stat, 0) != 0 for stat in ['work_efficiency', 'double_action', 'steps_add', 'steps_percent']):
                if stats.get('work_efficiency', 0) >= 0:
                    has_stats = True
                    break
            else:
                has_stats = True
                break
        
        if has_stats:
            all_items.append(item)
    
    # Keep highest quality only
    all_items = filter_items_by_quality(all_items, keep_highest_only=True)
    
    # Prepare items by slot
    items_by_slot = {}
    for slot in all_slots:
        items_by_slot[slot] = []
        for item in all_items:
            if slot.startswith('tool') and item.slot == 'tools':
                items_by_slot[slot].append(item)
            elif slot.startswith('ring') and item.slot == 'ring':
                items_by_slot[slot].append(item)
            elif item.slot == slot:
                items_by_slot[slot].append(item)
    
    # Optimize using segments
    gearset, metrics, iterations = optimize_single_route(
        route=None,
        character=character,
        all_items=all_items,
        items_by_slot=items_by_slot,
        gear_slots=gear_slots,
        tool_slots=tool_slots,
        all_slots=all_slots,
        segments=segments
    )
    
    return gearset, metrics, iterations


