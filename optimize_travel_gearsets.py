def _resolve_location_from_id(loc_id: str):
    """Resolve a lowercase location ID to a LocationInfo object."""
    from util.autogenerated.locations import Location, LocationInfo
    attr = loc_id.upper()
    loc = getattr(Location, attr, None)
    if isinstance(loc, LocationInfo):
        return loc
    return None


def _segments_to_routes(segments: List[dict]) -> List[Tuple]:
    """Convert API segment dicts to (Location, Location) tuples for RAW_ROUTES lookup."""
    routes = []
    for seg in segments:
        start_loc = _resolve_location_from_id(seg['start'])
        end_loc = _resolve_location_from_id(seg['end'])
        if start_loc and end_loc:
            routes.append((start_loc, end_loc))
    return routes


def _get_all_segment_requirements(segments: List[dict]) -> dict:
    """Collect all keyword requirements across all segments in a route."""
    combined = {}
    for seg in segments:
        kw_counts = seg.get('requirements', {}).get('keyword_counts', {})
        for keyword, count in kw_counts.items():
            combined[keyword] = max(combined.get(keyword, 0), count)
    return combined


def meets_segments_requirements(gearset_dict: dict, segments: List[dict]) -> bool:
    """Check if gearset meets all requirements across all segments."""
    items = [item for item in gearset_dict.values() if item is not None]
    
    for seg in segments:
        kw_counts = seg.get('requirements', {}).get('keyword_counts', {})
        for keyword, required_count in kw_counts.items():
            if required_count <= 0:
                continue
            count = sum(
                1 for item in items
                if hasattr(item, 'keywords') and
                any(keyword.lower() in kw.lower() for kw in item.keywords)
            )
            if count < required_count:
                return False
    return True


